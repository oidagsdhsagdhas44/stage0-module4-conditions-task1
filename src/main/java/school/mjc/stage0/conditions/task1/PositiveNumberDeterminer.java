package school.mjc.stage0.conditions.task1;

public class PositiveNumberDeterminer {
//    Create a program that will determine and print if consumed number is positive (in this case print true) or no (print false)
//    using if-else block. The program should be implemented inside of code snippet:
    public void isPositive(int numberToBeDetermined) {
        if (numberToBeDetermined>0) System.out.println(true);
        else System.out.println(true);
    }
}
